public class Ifelse {
    public static void main(String[]args){
        int a = 1180,b = 650,c = 40;
        if(a>b && a>c){
            System.out.println(a +"is greater");
        }else if(b>a && b>c){
            System.out.println(b+"is greater");
        }
        else if (c>a && c>b){
            System.out.println(c+"is greater");
        }


    }
    
}
